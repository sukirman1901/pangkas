// Shared Utilities
// ==========================================

export function updateContextMarkdown(projectRoot, data) {
  const dir = getMemoryDir(projectRoot);
  ensureDir(dir);
  const mdPath = path.join(dir, CONTEXT_FILE);
  
  const lines = [
    '# Pangkas Session Context',
    '',
    `> Auto-generated by Pangkas. Last updated: ${data.lastUpdated || new Date().toISOString()}`,
    '',
  ];
  
  // v2.0 format: show facts grouped by type
  if (data.version === '2.0' && Array.isArray(data.facts)) {
    const activeFacts = data.facts.filter(f => f.isLatest);
    
    if (activeFacts.length === 0) {
      lines.push('No active facts recorded yet.');
    } else {
      // Group by type
      const grouped = {};
      for (const f of activeFacts) {
        if (!grouped[f.type]) grouped[f.type] = [];
        grouped[f.type].push(f);
      }
      
      const typeOrder = ['bug', 'todo', 'decision', 'preference', 'question', 'file', 'done'];
      for (const type of typeOrder) {
        if (!grouped[type]) continue;
        lines.push(`## ${type.toUpperCase()} (${grouped[type].length})`);
        for (const f of grouped[type]) {
          const confidence = f.confidence > 0.8 ? ' ★' : '';
          lines.push(`- ${f.content}${confidence}`);
        }
        lines.push('');
      }
    }
  } else {
    // v1.x fallback
    lines.push('## Current Focus');
    lines.push(data.recentFocus || 'N/A');
    lines.push('');
    lines.push('## Recent Session Summary');
    lines.push(data.sessionSummary || 'N/A');
  }
  
  fs.writeFileSync(mdPath, lines.join('\n'));
}

export function ensureGitignore(projectRoot) {
  const gitignorePath = path.join(projectRoot, '.gitignore');
  const entry = '.pangkas/';
  let content = '';
  try {
    content = fs.readFileSync(gitignorePath, 'utf8');
  } catch {
    // file does not exist
  }
  if (content.includes(entry)) return;
  const separator = content.length > 0 && !content.endsWith('\n') ? '\n' : '';
  fs.writeFileSync(gitignorePath, `${content}${separator}${entry}\n`);
}
