/**
 * @deprecated Use saveFacts() instead
 */
export function saveMemory(projectRoot, data) {
  console.warn('[Pangkas] saveMemory() is deprecated. Use saveFacts() instead.');
  
  // Convert old format to facts if possible
  const facts = [];
  if (data.sessionSummary) {
    facts.push({
      id: 'legacy-' + Date.now(),
      type: 'decision',
      content: data.sessionSummary.slice(0, 150),
      confidence: 0.5,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
      sessionCount: 1,
      isLatest: true,
    });
  }
  if (data.recentFocus && data.recentFocus !== data.sessionSummary) {
    facts.push({
      id: 'legacy-focus-' + Date.now(),
      type: 'todo',
      content: data.recentFocus.slice(0, 150),
      confidence: 0.5,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
      sessionCount: 1,
      isLatest: true,
    });
  }
  if (Array.isArray(data.keyDecisions)) {
    for (const d of data.keyDecisions) {
      facts.push({
        id: 'legacy-decision-' + Date.now() + '-' + Math.random().toString(36).slice(2, 7),
        type: 'decision',
        content: d.slice(0, 150),
        confidence: 0.8,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        sessionCount: 1,
        isLatest: true,
      });
    }
  }
  if (Array.isArray(data.filesModified)) {
    for (const f of data.filesModified) {
      facts.push({
        id: 'legacy-file-' + Date.now() + '-' + Math.random().toString(36).slice(2, 7),
        type: 'file',
        content: f,
        confidence: 0.9,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        sessionCount: 1,
        isLatest: true,
      });
    }
  }
  
  saveFacts(projectRoot, facts);
}

